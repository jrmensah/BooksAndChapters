package com.company;


public class Chapters {
    private String title;
    private String page;

    public Chapters()
    {
        this.title = title;
        this.page = page;
    }

    public String getTitle()
    {
        return title;
    }

    public void setTitle(String title)
    {
        this.title = title;
    }

    public String getPage()
    {
        return page;
    }

    public void setPage(String page)
    {
        this.page = page;
    }


}
